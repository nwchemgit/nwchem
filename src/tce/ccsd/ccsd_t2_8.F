               end if
               CALL TCE_SORT_4(f_t,f_b,
     &              int_mb(k_range+p3b-1),int_mb(k_range+p4b-1),
     &              int_mb(k_range+p5b-1),int_mb(k_range+p6b-1),
     &              2,1,4,3,1.0d0)
               if (p5b .eq. p6b) then
