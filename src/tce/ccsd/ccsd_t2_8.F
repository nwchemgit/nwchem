     &              int_mb(k_a_offset),(h2b_1-1+noab*(h1b_1-1+noab*
     &              (p6b_1-noab-1+nvab*(p5b_1-noab-1)))))
               CALL TCE_SORT_4(f_t,f_a,
     &              int_mb(k_range+p5b-1),int_mb(k_range+p6b-1),
     &              int_mb(k_range+h1b-1),int_mb(k_range+h2b-1),
     &              4,3,2,1,1.0d0)
